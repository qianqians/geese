    #[test]
    fn test_output_order() {
        let mut builder = ComposedShaderBuilder::new("order_test");
        builder.add_stream(presets::position()).unwrap();
        builder.add_constant(ConstantDef {
            name: "k".into(),
            ty: WgslType::F32,
            id: 0,
            default_value: Some("1.0".into()),
        });
        builder.add_struct(StructDef {
            name: "MyStruct".into(),
            fields: vec![StructField {
                name: "value".into(),
                ty: WgslType::F32,
                attributes: vec![],
            }],
        });
        builder.add_function(FunctionDef {
            name: "helper".into(),
            parameters: vec![],
            return_type: None,
            body: WgslFragment::new("let x = 1.0;"),
            overridable: false,
        });
        builder.vertex_entry("vs_main", WgslFragment::new("output.clip_position = vec4<f32>(input.position, 1.0);"));
        builder.fragment_entry("fs_main", WgslFragment::new("return vec4<f32>(1.0);"));
        let shader = builder.build();

        let generator = WgslGenerator::new();
        let wgsl = generator.generate(&shader).unwrap();

        let header_pos = wgsl.find("// Generated").unwrap();
        let const_pos = wgsl.find("override").unwrap();
        let struct_pos = wgsl.find("struct MyStruct").unwrap();
        let func_pos = wgsl.find("fn helper").unwrap();
        let vs_pos = wgsl.find("@vertex").unwrap();
        let fs_pos = wgsl.find("@fragment").unwrap();

        assert!(header_pos < const_pos);
        assert!(const_pos < struct_pos);
        assert!(struct_pos < func_pos);
        assert!(func_pos < vs_pos);
        assert!(vs_pos < fs_pos);
    }

