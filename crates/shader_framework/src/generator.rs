//! WGSL code generation — produces valid, compilable WGSL from composed shader descriptions.

use crate::core::*;
use crate::stream::*;
use std::collections::HashMap;

// ─── Composed Shader ─────────────────────────────────────────────────────────

/// A fully composed shader description ready for WGSL generation.
#[derive(Debug, Clone)]
pub struct ComposedShader {
    pub name: String,
    pub streams: StreamRouter,
    pub bindings: Vec<ShaderBinding>,
    pub structs: Vec<StructDef>,
    pub functions: Vec<FunctionDef>,
    pub vertex_entry: Option<EntryPointDef>,
    pub fragment_entry: Option<EntryPointDef>,
    pub compute_entry: Option<ComputeEntryPointDef>,
    pub constants: Vec<ConstantDef>,
    pub global_vars: Vec<GlobalVarDef>,
}

// ─── Entry Point Types ───────────────────────────────────────────────────────

/// Entry point definition for vertex/fragment stages.
#[derive(Debug, Clone)]
pub struct EntryPointDef {
    pub name: String,
    pub body: WgslFragment,
    /// Additional local variables declared inside the function: (name, type, optional init).
    pub local_vars: Vec<(String, WgslType, Option<String>)>,
}

/// Compute shader entry point.
#[derive(Debug, Clone)]
pub struct ComputeEntryPointDef {
    pub name: String,
    pub workgroup_size: [u32; 3],
    pub body: WgslFragment,
    pub local_vars: Vec<(String, WgslType, Option<String>)>,
}

/// Pipeline-overridable constant.
#[derive(Debug, Clone)]
pub struct ConstantDef {
    pub name: String,
    pub ty: WgslType,
    pub id: u32,
    pub default_value: Option<String>,
}

/// Global variable (non-binding, e.g. `var<private>`).
#[derive(Debug, Clone)]
pub struct GlobalVarDef {
    pub name: String,
    pub ty: WgslType,
    pub address_space: AddressSpace,
    pub init: Option<String>,
}

/// Address space for global variables.
#[derive(Debug, Clone, PartialEq, Eq)]
pub enum AddressSpace {
    Private,
    Workgroup,
}

impl AddressSpace {
    /// WGSL keyword for this address space.
    pub fn to_wgsl(&self) -> &'static str {
        match self {
            Self::Private => "private",
            Self::Workgroup => "workgroup",
        }
    }
}

// ─── WGSL Generator ──────────────────────────────────────────────────────────

/// The main WGSL code generator.
///
/// Takes a [`ComposedShader`] and produces a complete, valid WGSL source string.
pub struct WgslGenerator;

impl WgslGenerator {
    pub fn new() -> Self {
        Self
    }

    /// Generate complete WGSL source from a composed shader.
    ///
    /// Output order:
    /// 1. Header comment
    /// 2. Pipeline-overridable constants
    /// 3. Struct definitions
    /// 4. Global variables (`var<private>`, `var<workgroup>`)
    /// 5. Binding declarations (`@group @binding`)
    /// 6. Helper/utility functions (non-entry-point)
    /// 7. Vertex entry point
    /// 8. Fragment entry point
    /// 9. Compute entry point
    pub fn generate(&self, shader: &ComposedShader) -> ShaderResult<String> {
        let mut parts: Vec<String> = Vec::new();

        // 1. Header
        parts.push(format!(
            "// Generated by shader_framework — {}\n",
            shader.name
        ));

        // 2. Constants
        let constants = self.generate_constants(&shader.constants);
        if !constants.is_empty() {
            parts.push(constants);
        }

        // 3. Structs
        let structs = self.generate_structs(&shader.structs);
        if !structs.is_empty() {
            parts.push(structs);
        }

        // 4. Global variables
        let globals = self.generate_global_vars(&shader.global_vars);
        if !globals.is_empty() {
            parts.push(globals);
        }

        // 5. Bindings
        let bindings = self.generate_bindings(&shader.bindings)?;
        if !bindings.is_empty() {
            parts.push(bindings);
        }

        // 6. Helper functions
        let functions = self.generate_functions(&shader.functions);
        if !functions.is_empty() {
            parts.push(functions);
        }

        // 7. Vertex entry point
        if let Some(ref entry) = shader.vertex_entry {
            let vs = self.generate_vertex_entry(&shader.streams, entry, &shader.bindings)?;
            parts.push(vs);
        }

        // 8. Fragment entry point
        if let Some(ref entry) = shader.fragment_entry {
            let fs = self.generate_fragment_entry(&shader.streams, entry, &shader.bindings)?;
            parts.push(fs);
        }

        // 9. Compute entry point
        if let Some(ref entry) = shader.compute_entry {
            let cs = self.generate_compute_entry(entry, &shader.bindings)?;
            parts.push(cs);
        }

        Ok(parts.join("\n"))
    }

    /// Generate only the struct definitions section.
    pub fn generate_structs(&self, structs: &[StructDef]) -> String {
        structs
            .iter()
            .map(|s| s.to_wgsl())
            .collect::<Vec<_>>()
            .join("\n")
    }

    /// Generate binding declarations, sorted by (group, binding).
    ///
    /// Detects conflicts (same group:binding used by different names).
    pub fn generate_bindings(&self, bindings: &[ShaderBinding]) -> ShaderResult<String> {
        // Check for conflicts.
        let mut seen: HashMap<(u32, u32), &str> = HashMap::new();
        for b in bindings {
            if let Some(existing) = seen.get(&(b.group, b.binding)) {
                if *existing != b.name.as_str() {
                    return Err(ShaderError::BindingConflict {
                        group: b.group,
                        binding: b.binding,
                        existing: existing.to_string(),
                        new: b.name.clone(),
                    });
                }
            } else {
                seen.insert((b.group, b.binding), &b.name);
            }
        }

        // Sort by (group, binding).
        let mut sorted: Vec<&ShaderBinding> = bindings.iter().collect();
        sorted.sort_by_key(|b| (b.group, b.binding));

        let lines: Vec<String> = sorted
            .iter()
            .map(|b| {
                let wgsl = b.to_wgsl();
                // Buffer bindings from to_wgsl() lack a trailing semicolon.
                if wgsl.ends_with(';') {
                    wgsl
                } else {
                    format!("{};", wgsl)
                }
            })
            .collect();

        Ok(lines.join("\n"))
    }

    /// Generate all helper functions (non-entry-point).
    pub fn generate_functions(&self, functions: &[FunctionDef]) -> String {
        functions
            .iter()
            .map(|f| f.to_wgsl())
            .collect::<Vec<_>>()
            .join("\n")
    }

    /// Generate pipeline-overridable constants.
    pub fn generate_constants(&self, constants: &[ConstantDef]) -> String {
        constants
            .iter()
            .map(|c| {
                let default = match &c.default_value {
                    Some(v) => format!(" = {}", v),
                    None => String::new(),
                };
                format!(
                    "override @id({}) {}: {}{};",
                    c.id,
                    c.name,
                    c.ty.to_wgsl(),
                    default
                )
            })
            .collect::<Vec<_>>()
            .join("\n")
    }

    /// Generate the vertex entry point function with proper I/O structs.
    ///
    /// 1. Generates `VertexInput` struct from router (VS-only + VS-to-FS streams as `@location` inputs).
    /// 2. Generates `VertexOutput` struct (`@builtin(position)` + VS-to-FS streams as `@location` outputs).
    /// 3. Generates `@vertex fn vs_main(input: VertexInput) -> VertexOutput { ... }`.
    pub fn generate_vertex_entry(
        &self,
        router: &StreamRouter,
        entry: &EntryPointDef,
        _bindings: &[ShaderBinding],
    ) -> ShaderResult<String> {
        let vs_input = router.generate_vs_input_struct("VertexInput");
        let vs_output = router.generate_vs_output_struct("VertexOutput");

        // Build function body.
        let mut body_lines = Vec::new();
        body_lines.push("var output: VertexOutput;".to_string());

        for (name, ty, init) in &entry.local_vars {
            match init {
                Some(v) => body_lines.push(format!("var {}: {} = {};", name, ty.to_wgsl(), v)),
                None => body_lines.push(format!("var {}: {};", name, ty.to_wgsl())),
            }
        }

        if !entry.body.is_empty() {
            body_lines.push(entry.body.source.clone());
        }

        body_lines.push("return output;".to_string());

        let body_str = body_lines.join("\n");
        let indented = WgslFragment::new(&body_str).indent(4);

        Ok(format!(
            "{}\n{}\n@vertex\nfn {}(input: VertexInput) -> VertexOutput {{\n{}\n}}\n",
            vs_input, vs_output, entry.name, indented
        ))
    }

    /// Generate the fragment entry point function with proper I/O.
    ///
    /// 1. Generates `FragmentInput` struct (matches VertexOutput).
    /// 2. Generates `@fragment fn fs_main(input: FragmentInput) -> @location(0) vec4<f32> { ... }`.
    pub fn generate_fragment_entry(
        &self,
        router: &StreamRouter,
        entry: &EntryPointDef,
        _bindings: &[ShaderBinding],
    ) -> ShaderResult<String> {
        let fs_input = router.generate_fs_input_struct("FragmentInput");

        let mut body_lines = Vec::new();

        for (name, ty, init) in &entry.local_vars {
            match init {
                Some(v) => body_lines.push(format!("var {}: {} = {};", name, ty.to_wgsl(), v)),
                None => body_lines.push(format!("var {}: {};", name, ty.to_wgsl())),
            }
        }

        if !entry.body.is_empty() {
            body_lines.push(entry.body.source.clone());
        }

        let body_str = body_lines.join("\n");
        let indented = WgslFragment::new(&body_str).indent(4);

        Ok(format!(
            "{}\n@fragment\nfn {}(input: FragmentInput) -> @location(0) vec4<f32> {{\n{}\n}}\n",
            fs_input, entry.name, indented
        ))
    }

    /// Generate the compute entry point function.
    pub fn generate_compute_entry(
        &self,
        entry: &ComputeEntryPointDef,
        _bindings: &[ShaderBinding],
    ) -> ShaderResult<String> {
        let mut body_lines = Vec::new();

        for (name, ty, init) in &entry.local_vars {
            match init {
                Some(v) => body_lines.push(format!("var {}: {} = {};", name, ty.to_wgsl(), v)),
                None => body_lines.push(format!("var {}: {};", name, ty.to_wgsl())),
            }
        }

        if !entry.body.is_empty() {
            body_lines.push(entry.body.source.clone());
        }

        let body_str = body_lines.join("\n");
        let indented = WgslFragment::new(&body_str).indent(4);

        let [x, y, z] = entry.workgroup_size;

        Ok(format!(
            "@compute @workgroup_size({}, {}, {})\nfn {}(@builtin(global_invocation_id) global_id: vec3<u32>) {{\n{}\n}}\n",
            x, y, z, entry.name, indented
        ))
    }

    /// Validate the composed shader before generation.
    pub fn validate(&self, shader: &ComposedShader) -> ShaderResult<()> {
        // At least one entry point.
        let has_graphics = shader.vertex_entry.is_some() && shader.fragment_entry.is_some();
        let has_compute = shader.compute_entry.is_some();
        if !has_graphics && !has_compute {
            return Err(ShaderError::Composition {
                message: "Shader must have at least one entry point \
                          (vertex+fragment OR compute)"
                    .into(),
            });
        }
        if has_compute && (has_graphics || shader.vertex_entry.is_some() || shader.fragment_entry.is_some()) {
            return Err(ShaderError::Composition {
                message: "Shader cannot have both compute and graphics entry points".into(),
            });
        }

        // Binding conflicts.
        let mut binding_map: HashMap<(u32, u32), &str> = HashMap::new();
        for b in &shader.bindings {
            if let Some(existing) = binding_map.get(&(b.group, b.binding)) {
                if *existing != b.name.as_str() {
                    return Err(ShaderError::BindingConflict {
                        group: b.group,
                        binding: b.binding,
                        existing: existing.to_string(),
                        new: b.name.clone(),
                    });
                }
            } else {
                binding_map.insert((b.group, b.binding), &b.name);
            }
        }

        // Stream router validation.
        shader.streams.validate()?;

        // Entry point body not empty.
        if let Some(ref entry) = shader.vertex_entry {
            if entry.body.is_empty() && entry.local_vars.is_empty() {
                return Err(ShaderError::Composition {
                    message: "Vertex entry point body is empty".into(),
                });
            }
        }
        if let Some(ref entry) = shader.fragment_entry {
            if entry.body.is_empty() && entry.local_vars.is_empty() {
                return Err(ShaderError::Composition {
                    message: "Fragment entry point body is empty".into(),
                });
            }
        }
        if let Some(ref entry) = shader.compute_entry {
            if entry.body.is_empty() && entry.local_vars.is_empty() {
                return Err(ShaderError::Composition {
                    message: "Compute entry point body is empty".into(),
                });
            }
        }

        // Referenced struct types must be defined.
        let defined: std::collections::HashSet<&str> =
            shader.structs.iter().map(|s| s.name.as_str()).collect();
        for b in &shader.bindings {
            match &b.resource_type {
                BindingResourceType::UniformBuffer | BindingResourceType::StorageBuffer { .. } => {
                    let struct_name = buffer_struct_name(&b.name);
                    if !defined.contains(struct_name.as_str()) {
                        return Err(ShaderError::Composition {
                            message: format!(
                                "Binding '{}' references struct '{}' which is not defined",
                                b.name, struct_name
                            ),
                        });
                    }
                }
                _ => {}
            }
        }

        Ok(())
    }

    /// Generate WGSL and validate it with naga's WGSL parser.
    pub fn generate_and_validate(&self, shader: &ComposedShader) -> ShaderResult<String> {
        let wgsl = self.generate(shader)?;
        match naga::front::wgsl::parse_str(&wgsl) {
            Ok(_module) => Ok(wgsl),
            Err(e) => Err(ShaderError::WgslValidation {
                message: format!("{}", e),
            }),
        }
    }

    fn generate_global_vars(&self, vars: &[GlobalVarDef]) -> String {
        generate_global_vars_section(vars)
    }
}

impl Default for WgslGenerator {
    fn default() -> Self {
        Self::new()
    }
}

/// Global variable generation helper.
fn generate_global_vars_section(vars: &[GlobalVarDef]) -> String {
    vars.iter()
        .map(|v| {
            let init = match &v.init {
                Some(i) => format!(" = {}", i),
                None => String::new(),
            };
            format!(
                "var<{}> {}: {}{};",
                v.address_space.to_wgsl(),
                v.name,
                v.ty.to_wgsl(),
                init
            )
        })
        .collect::<Vec<_>>()
        .join("\n")
}

/// Derive the conventional struct name for a buffer binding (matching `ShaderBinding::struct_name_for_buffer`).
fn buffer_struct_name(binding_name: &str) -> String {
    let mut s = binding_name.to_string();
    if let Some(first) = s.get_mut(0..1) {
        first.make_ascii_uppercase();
    }
    format!("{}Data", s)
}

// ─── Builder ─────────────────────────────────────────────────────────────────

/// Builder for constructing [`ComposedShader`] instances.
#[derive(Debug, Clone)]
pub struct ComposedShaderBuilder {
    name: String,
    streams: StreamRouter,
    bindings: Vec<ShaderBinding>,
    structs: Vec<StructDef>,
    functions: Vec<FunctionDef>,
    vertex_entry: Option<EntryPointDef>,
    fragment_entry: Option<EntryPointDef>,
    compute_entry: Option<ComputeEntryPointDef>,
    constants: Vec<ConstantDef>,
    global_vars: Vec<GlobalVarDef>,
}

impl ComposedShaderBuilder {
    pub fn new(name: impl Into<String>) -> Self {
        Self {
            name: name.into(),
            streams: StreamRouter::new(),
            bindings: Vec::new(),
            structs: Vec::new(),
            functions: Vec::new(),
            vertex_entry: None,
            fragment_entry: None,
            compute_entry: None,
            constants: Vec::new(),
            global_vars: Vec::new(),
        }
    }

    pub fn add_stream(&mut self, decl: StreamDeclaration) -> ShaderResult<&mut Self> {
        self.streams.add_stream(decl)?;
        Ok(self)
    }

    pub fn add_streams(&mut self, decls: Vec<StreamDeclaration>) -> ShaderResult<&mut Self> {
        self.streams.add_streams(decls)?;
        Ok(self)
    }

    pub fn add_binding(&mut self, binding: ShaderBinding) -> &mut Self {
        self.bindings.push(binding);
        self
    }

    pub fn add_struct(&mut self, s: StructDef) -> &mut Self {
        self.structs.push(s);
        self
    }

    pub fn add_function(&mut self, f: FunctionDef) -> &mut Self {
        self.functions.push(f);
        self
    }

    pub fn vertex_entry(&mut self, name: impl Into<String>, body: WgslFragment) -> &mut Self {
        self.vertex_entry = Some(EntryPointDef {
            name: name.into(),
            body,
            local_vars: Vec::new(),
        });
        self
    }

    pub fn fragment_entry(&mut self, name: impl Into<String>, body: WgslFragment) -> &mut Self {
        self.fragment_entry = Some(EntryPointDef {
            name: name.into(),
            body,
            local_vars: Vec::new(),
        });
        self
    }

    pub fn compute_entry(
        &mut self,
        name: impl Into<String>,
        workgroup_size: [u32; 3],
        body: WgslFragment,
    ) -> &mut Self {
        self.compute_entry = Some(ComputeEntryPointDef {
            name: name.into(),
            workgroup_size,
            body,
            local_vars: Vec::new(),
        });
        self
    }

    pub fn add_constant(&mut self, c: ConstantDef) -> &mut Self {
        self.constants.push(c);
        self
    }

    pub fn add_global_var(&mut self, v: GlobalVarDef) -> &mut Self {
        self.global_vars.push(v);
        self
    }

    pub fn build(self) -> ComposedShader {
        ComposedShader {
            name: self.name,
            streams: self.streams,
            bindings: self.bindings,
            structs: self.structs,
            functions: self.functions,
            vertex_entry: self.vertex_entry,
            fragment_entry: self.fragment_entry,
            compute_entry: self.compute_entry,
            constants: self.constants,
            global_vars: self.global_vars,
        }
    }
}

